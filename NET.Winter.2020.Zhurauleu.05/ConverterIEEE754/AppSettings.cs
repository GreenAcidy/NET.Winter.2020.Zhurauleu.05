namespace ConverterMethods
{
    /// <summary >
    /// Class AppSettings. Contains two fileds which will be used for constants.
    /// </summary >
    public class AppSettings
    {
        /// <summary >
        /// Gets or sets field BytesInLong.
        /// </summary >
        /// <value>
        /// Field Epsilon represent count of bytes in long type.
        /// </value>
        public int BytesInLong { get; set; }

        /// <summary >
        /// Gets or sets field BitsInByte.
        /// </summary >
        /// <value>
        /// Field BitsIiByte represent number of of bits in byteб giving the ability to compute with any number of bytes.
        /// </value>
        public int BitsInByte { get; set; }
    }
}
